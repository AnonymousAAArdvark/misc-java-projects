package AndrewY;

public class Items {
	private String ID;
	private String description;
	public Items() {
		ID = null;
		description = null;
	}
	String getID() {
		return ID;
	}
	String getDesc() {
		return description;
	}
	void setID(String i) {
		ID = i;
	}
	void setDesc(String desc)  {
		description = desc;
	}
	
}
